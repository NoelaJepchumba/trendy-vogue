function toggleDressInfo() {
    var dressInfo = document.getElementById("dressInfo");
    dressInfo.style.display = (dressInfo.style.display === "none") ? "block" : "none";
}

function toggleSkirtInfo() {
    var skirtInfo = document.getElementById("skirtInfo");
    skirtInfo.style.display = (skirtInfo.style.display === "none") ? "block" : "none";
}